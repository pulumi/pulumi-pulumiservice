// Command scaffold-metadata produces candidate metadata.json entries from
// the embedded OpenAPI spec.
//
// It walks every operationId, splits each into a verb + noun via prefix
// match, groups by noun, and emits an entry for each noun that has at
// least Create + Get (or Update + Get under the upsert pattern).
//
// Output is metadata.candidate.json — committed, regenerated by go generate
// alongside spec.json. metadata.json itself is hand-curated; humans copy
// entries from the candidate file as they decide which resources to expose.
//
// Field-level metadata (forceNew/secret/aliases/renames) is intentionally
// NOT inferred — that requires per-resource judgment and lives in the
// curated metadata.json.
package main

import (
	"encoding/json"
	"flag"
	"fmt"
	"maps"
	"os"
	"sort"
	"strings"
	"unicode"
)

// verb prefixes mapped to CRUD slots. Order matters: longest first so
// "BatchCreate" wins over "Create".
var verbPrefixes = []struct {
	prefix string
	slot   string
}{
	{"BatchCreate", "create"},
	{"BatchUpdate", "update"},
	{"BatchDelete", "delete"},
	{"Create", "create"},
	{"Update", "update"},
	{"Delete", "delete"},
	{"Patch", "update"},
	{"Replace", "update"},
	{"Put", "update"},
	{"Get", "read"},
	{"Read", "read"},
	{"Describe", "read"},
}

// nonCRUDVerbs are recognized so noun extraction works on them, but they
// don't map to a CRUD slot. Surfaced as "_unmapped" on the relevant
// resource entry for awareness.
var nonCRUDVerbs = []string{
	"List", "Search", "Find", "Add", "Remove", "Cancel", "Approve",
	"Reject", "Reset", "Refresh", "Restore", "Validate", "Trigger",
	"Poll", "Open", "Close", "Encrypt", "Decrypt", "Apply", "Complete",
	"Append", "Bulk", "Accept",
}

func main() {
	in := flag.String("in", "spec.json", "Input OpenAPI spec path")
	out := flag.String("out", "metadata.candidate.json", "Output candidate metadata path")
	flag.Parse()

	data, err := os.ReadFile(*in)
	if err != nil {
		fmt.Fprintf(os.Stderr, "scaffold-metadata: read spec: %v\n", err)
		os.Exit(1)
	}

	var spec struct {
		Paths map[string]map[string]any `json:"paths"`
	}
	if err := json.Unmarshal(data, &spec); err != nil {
		fmt.Fprintf(os.Stderr, "scaffold-metadata: parse spec: %v\n", err)
		os.Exit(1)
	}

	type ops = map[string]string // slot -> opId
	byNoun := map[string]ops{}
	otherOps := map[string][]string{}
	totalOps := 0

	for _, item := range spec.Paths {
		for method, op := range item {
			obj, ok := op.(map[string]any)
			if !ok {
				continue
			}
			id, _ := obj["operationId"].(string)
			if id == "" {
				continue
			}
			_ = method
			totalOps++

			verb, noun, slot := splitOperationID(id)
			if slot != "" {
				if _, exists := byNoun[noun]; !exists {
					byNoun[noun] = ops{}
				}
				if _, set := byNoun[noun][slot]; !set {
					byNoun[noun][slot] = id
				}
			} else if verb != "" {
				otherOps[noun] = append(otherOps[noun], id)
			}
		}
	}

	type entry struct {
		Operations map[string]string `json:"operations"`
		Upsert     bool              `json:"_upsert,omitempty"`
		Unmapped   []string          `json:"_unmapped,omitempty"`
	}

	candidates := map[string]entry{}
	type skip struct {
		noun  string
		slots []string
	}
	var skipped []skip

	for noun, o := range byNoun {
		final := maps.Clone(o)
		upsert := false
		// Upsert pattern: PUT /resource/{id} acts as create+update. Reuse
		// update for create when create isn't otherwise present.
		if _, hasCreate := final["create"]; !hasCreate {
			if u, hasUpdate := final["update"]; hasUpdate {
				if _, hasRead := final["read"]; hasRead {
					final["create"] = u
					upsert = true
				}
			}
		}
		_, hasCreate := final["create"]
		_, hasRead := final["read"]
		if hasCreate && hasRead {
			tok := "pulumiservice:v2:" + noun
			e := entry{Operations: final, Upsert: upsert}
			if extras := otherOps[noun]; len(extras) > 0 {
				dedup := map[string]struct{}{}
				for _, x := range extras {
					dedup[x] = struct{}{}
				}
				keys := make([]string, 0, len(dedup))
				for k := range dedup {
					keys = append(keys, k)
				}
				sort.Strings(keys)
				e.Unmapped = keys
			}
			candidates[tok] = e
		} else {
			slots := make([]string, 0, len(o))
			for k := range o {
				slots = append(slots, k)
			}
			sort.Strings(slots)
			skipped = append(skipped, skip{noun, slots})
		}
	}

	doc := map[string]any{
		"version":         1,
		"package":         "pulumiservice",
		"_generated_from": "spec.json via provider/tools/scaffold-metadata",
		"_note": "Candidate entries — review and copy desired entries into metadata.json. " +
			"Field-level metadata (forceNew/secret/aliases/renames) is NOT inferred. " +
			"_upsert flags entries where Create reuses the Update operation (PUT-style API). " +
			"_unmapped lists non-CRUD operations on the same noun (List/Search/etc.).",
		"resources": candidates,
	}

	encoded, err := json.MarshalIndent(doc, "", "  ")
	if err != nil {
		fmt.Fprintf(os.Stderr, "scaffold-metadata: encode: %v\n", err)
		os.Exit(1)
	}
	if err := os.WriteFile(*out, append(encoded, '\n'), 0o644); err != nil {
		fmt.Fprintf(os.Stderr, "scaffold-metadata: write %s: %v\n", *out, err)
		os.Exit(1)
	}

	sort.Slice(skipped, func(i, j int) bool { return len(skipped[i].slots) > len(skipped[j].slots) })
	fmt.Printf("scaffold-metadata: wrote %s\n", *out)
	fmt.Printf("  operations in spec:    %d\n", totalOps)
	fmt.Printf("  scaffolded resources:  %d\n", len(candidates))
	fmt.Printf("  skipped (one of Create/Get missing): %d\n", len(skipped))
}

// splitOperationID returns (verb, noun, slot). slot is "" when the verb
// isn't a CRUD verb (e.g., List, Cancel). verb is "" when no recognized
// prefix matches.
func splitOperationID(id string) (verb, noun, slot string) {
	for _, vp := range verbPrefixes {
		if matchPrefix(id, vp.prefix) {
			return vp.prefix, id[len(vp.prefix):], vp.slot
		}
	}
	for _, v := range nonCRUDVerbs {
		if matchPrefix(id, v) {
			return v, id[len(v):], ""
		}
	}
	return "", id, ""
}

// matchPrefix checks that prefix is at the start of id AND the next
// character is uppercase (so "Update" matches "UpdateStack" but not
// "Updater"). Returns false if prefix consumes the whole string.
func matchPrefix(id, prefix string) bool {
	if !strings.HasPrefix(id, prefix) {
		return false
	}
	if len(id) == len(prefix) {
		return false
	}
	return unicode.IsUpper(rune(id[len(prefix)]))
}
